def get_fish_by_id(fish_id):
    for fish in fish_data:
        if fish["id"] == fish_id:
            return fish
    return None

def get_fish_by_name(fish_name):
    for fish in fish_data:
        if fish["name"].lower() == fish_name.lower():
            return fish
    return None

def search_fish(query):
    query = query.lower()
    return [f for f in fish_data if query in f["name"].lower() or query in f["description"].lower()]
